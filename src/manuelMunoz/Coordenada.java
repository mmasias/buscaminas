class Coordenada {
    private int coordenadaX;
    private int coordenadaY;
    
    public Coordenada(int coordenadaX, int coordenadaY){
        this.coordenadaX = coordenadaX;
        this.coordenadaY = coordenadaY;
    }

    public int CoordenadaX(){
        return coordenadaX;
    }

    public int CoordenadaY(){
        return coordenadaY;
    }

}